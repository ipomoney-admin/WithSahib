/**
 * Strip Fyers prefix/suffix to get a plain ticker name.
 * NSE:RELIANCE-EQ → RELIANCE
 */
export function fyersToTicker(fyersSymbol: string): string {
  return fyersSymbol.replace(/^NSE:/, '').replace(/-EQ$/, '')
}
